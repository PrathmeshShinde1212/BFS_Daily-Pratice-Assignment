#int
a=12;
print(type(a));

#float
b=12.9;
print(type(b));

#complex
c=12+8j;
print(type(c));

#str
d="Prathmesh";
print(type(d));

#bool
e=True;
print(type(e));

#List
f=[1,2,3,4,5];
print(type(f));

#tuple
g=(1,2,3,4,5);
print(type(g));

#set
h={1,2,3,5};
print(type(h));

#frozenset
i=frozenset({1,2,3,4,5});
print(type(i));

#dict
j={'id':12,'name':'Prathmesh'};
print(type(j));

#none
k=None;
print(type(k));
